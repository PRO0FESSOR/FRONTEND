

// array push pop

let nums = [1,2,3,4];
console.log(nums);
nums.push(5);
console.log(nums);
let popedEle = nums.pop();
console.log(`popped number is ${popedEle}`);
console.log(nums);

//arra shift unshift - to add remove in starting of array

let ages = [1,2,3,4];
console.log(ages);
ages.unshift(5);
console.log(ages);
let num = ages.shift();
console.log(`shifted element is - ${num}`);
console.log(ages);
